print('**************************')
# == : return true if the value of n1 is equal to n2.
print(f"{n1}=={n2}:{n1 == n2}")

# != : return true if the value of n1 is not equal to n2.
print(f"{n1}!={n2}:{n1 != n2}")

# > : return true if the value of n1 is greater than n2.
print(f"{n1}>{n2}:{n1 > n2}")

# < : return true if the value of n1 is less than n2.
print(f"{n1}<{n2}:{n1 < n2}")

# >= : == or >
print(f"{n1}>={n2}:{n1 >= n2}")

# <= : == or <
print(f"{n1}<={n2}:{n1 <= n2}")

